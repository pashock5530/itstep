public class People {

    private String name;
    private String sname;
    private String male;

    public People(String name, String sname, String male) {
        this.name = name;
        this.sname = sname;
        this.male = male;
    }

    public String getName() {
        return name;
    }

    public String getSname() {
        return sname;
    }

    public String getMale() {
        return male;
    }

}
